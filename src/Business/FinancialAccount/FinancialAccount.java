package Business.FinancialAccount;

/**
 *
 * @author AnanthaShankar
 */
public class FinancialAccount {
    private String accountHolderName = "Default";
    private String uniqueId = "Default";
    private String accountType = "Default";
    private String bankName = "Default";

    public void FinancialAccount() {
        
    }
    
    public String getAccountHolderName() {
        return accountHolderName;
    }

    public void setAccountHolderName(String accountHolderName) {
        this.accountHolderName = accountHolderName;
    }

    public String getUniqueId() {
        return uniqueId;
    }

    public void setUniqueId(String uniqueId) {
        this.uniqueId = uniqueId;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    @Override
    public String toString() {
        return accountHolderName;
    }
    
    
    
}
