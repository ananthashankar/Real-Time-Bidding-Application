    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        if(organizationJComboBox.getItemCount() > 0 && employeeJComboBox.getItemCount() > 0 
                && roleJComboBox.getItemCount() > 0){
        if (nameJTextField.getText().trim().length() > 0
                && passwordJTextField.getText().trim().length() > 0) {
            String userName = nameJTextField.getText().trim();
            String password = passwordJTextField.getText().trim();
            Enterprise inEnterprise = null;
            Organization inOrganization = null;
            UserAccount userAccount = system.getUserAccountDirectory().searchUser(userName);
            if (userAccount == null) {
                    for (Network n : system.getNetworkList()) {
                        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                            userAccount = e.getUserAccountDirectory().searchUser(userName);
                            if (userAccount == null) {
                                for (Organization o : e.getOrganizationDirectory().getOrgList()) {
                                    userAccount = o.getUserAccountDirectory().searchUser(userName);
                                    if (userAccount != null) {
                                        inOrganization = o;
                                        inEnterprise = e;

                                        break;
                                    }

                                }
                            } else {
                                inEnterprise = e;
                                break;
                            }
                            if (inOrganization != null) {
                                break;
                            }
                        }
                        if (inEnterprise != null) {
                            break;
                        }
                    }

                }

            if (userAccount == null) {
            for (Bank b : system.getBankDirectory().getBankList()) {
                userAccount = b.getUserAccountDirectory().searchUser(userName);
                if (userAccount == null) {
                    for (Organization o : b.getOrganizationDirectory().getOrgList()) {
                        userAccount = o.getUserAccountDirectory().searchUser(userName);
                        if (userAccount != null) {
                            break;
                        }
                    }
                } else {
                    break;
                }
            } 
        }

            if (userAccount == null) {
                for (AdExchange ae : system.getAdExDir().getAdExchangeList()) {
                    userAccount = ae.getUserAccountDirectory().searchUser(userName);
                    if (userAccount == null) {
                        for (Organization o : ae.getOrganizationDirectory().getOrgList()) {
                            userAccount = o.getUserAccountDirectory().searchUser(userName);
                            if (userAccount != null) {
                                break;
                            }
                        }
                    } else {
                        break;
                    }
                } 
            }
            if (userAccount == null) {

                Organization organization = (Organization) organizationJComboBox.getSelectedItem();
                Person p = (Person) employeeJComboBox.getSelectedItem();
                Role role = (Role) roleJComboBox.getSelectedItem();
                organization.getUserAccountDirectory().createUserAccount(userName, password, p, role);
                popData();
                JOptionPane.showMessageDialog(null, "User Account Successfully Created");
            } else {
                JOptionPane.showMessageDialog(null, "Username already exists. Please use different Username");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Enter Values for Username or Password or Name");
        }
        }
        else {
            JOptionPane.showMessageDialog(null, "Invalid Creation");
        }

    }//GEN-LAST:event_createUserJButtonActionPerformed

